/**
 * Returns the localized name for a currency code.
 * Falls back to the Swedish/default name passed as `fallback`.
 */
export function getLocalizedName(
  code: string,
  lang: Language,
  fallback: string,
): string {
  if (lang === 'sv') return fallback;
  const entry = NAMES[code];
  if (!entry) return fallback;
  return entry[lang] ?? fallback;
}
